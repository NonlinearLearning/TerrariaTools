                    "controlflow"),
                new CpgEdgeSchema(
                    "POST_DOMINATE",
                    "PostDominateEdge",
                    ["METHOD", "METHOD_PARAMETER_IN", "METHOD_PARAMETER_OUT", "METHOD_RETURN", "LOCAL", "CALL", "CONTROL_STRUCTURE", "RETURN"],
                    ["METHOD", "METHOD_PARAMETER_IN", "METHOD_PARAMETER_OUT", "METHOD_RETURN", "LOCAL", "CALL", "CONTROL_STRUCTURE", "RETURN"],
                    "controlflow"),
                new CpgEdgeSchema(
                    "CDG",
                    "CdgEdge",
                    ["METHOD", "CONTROL_STRUCTURE"],
                    ["CALL", "CONTROL_STRUCTURE", "RETURN"],
                    "controlflow"),
                new CpgEdgeSchema(
                    "INHERITS_FROM",
                    "InheritsFromEdge",
                    ["TYPE_DECL"],
                    ["TYPE_DECL", "TYPE"],
                    "typerel"),
                new CpgEdgeSchema(
                    "ALIAS_OF",
                    "AliasOfEdge",
                    ["MEMBER", "METHOD_PARAMETER_IN", "METHOD_PARAMETER_OUT", "METHOD_RETURN", "LOCAL", "TYPE_REF"],
                    ["TYPE"],
                    "typerel"),
                new CpgEdgeSchema(
                    "FIELD_ACCESS",
                    "FieldAccessEdge",
                    ["FIELD_IDENTIFIER"],
                    ["MEMBER"],
                    "typerel"),
                new CpgEdgeSchema(
                    "CALL",
                    "CallEdge",
                    ["METHOD"],
                    ["METHOD"],
                    "callgraph"),
            ],
            Properties:
            [
                new CpgPropertySchema("LANGUAGE", "string"),
                new CpgPropertySchema("OVERLAYS", "string[]"),
                new CpgPropertySchema("ROOT", "string"),
                new CpgPropertySchema("VERSION", "string"),
                new CpgPropertySchema("PATH", "string"),
                new CpgPropertySchema("NAME", "string"),
                new CpgPropertySchema("FULL_NAME", "string"),
                new CpgPropertySchema("SIGNATURE", "string"),
                new CpgPropertySchema("METHOD_FULL_NAME", "string"),
                new CpgPropertySchema("TYPE_FULL_NAME", "string"),
                new CpgPropertySchema("METHOD_NAME", "string"),
                new CpgPropertySchema("CONTAINING_TYPE_NAME", "string"),
                new CpgPropertySchema("RETURN_TYPE_NAME", "string"),
                new CpgPropertySchema("BASE_TYPE_NAME", "string"),
                new CpgPropertySchema("OWNER_METHOD_NAME", "string"),
                new CpgPropertySchema("TARGET_METHOD_NAME", "string"),
                new CpgPropertySchema("RESOLVED_TARGET_METHOD_ID", "string"),
                new CpgPropertySchema("ORDER", "int"),
                new CpgPropertySchema("CODE", "string"),
                new CpgPropertySchema("CONTROL_STRUCTURE_TYPE", "string"),
            ]);
    }
}
