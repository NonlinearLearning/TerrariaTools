                    "ARGUMENT",
                    "ArgumentEdge",
                    ["CALL"],
                    ["IDENTIFIER", "LITERAL", "CALL", "METHOD_REF", "FIELD_IDENTIFIER"],
                    "base"),
                new CpgEdgeSchema(
                    "RECEIVER",
                    "ReceiverEdge",
                    ["CALL"],
                    ["IDENTIFIER", "FIELD_IDENTIFIER"],
                    "base"),
                new CpgEdgeSchema(
                    "REF",
                    "RefEdge",
                    ["IDENTIFIER", "FIELD_IDENTIFIER", "METHOD_REF", "TYPE_REF", "MEMBER", "METHOD_PARAMETER_IN", "METHOD_PARAMETER_OUT", "METHOD_RETURN", "LOCAL"],
                    ["METHOD", "MEMBER", "METHOD_PARAMETER_IN", "METHOD_PARAMETER_OUT", "LOCAL", "TYPE"],
                    "base"),
                new CpgEdgeSchema(
                    "SOURCE_FILE",
                    "SourceFileEdge",
                    ["NAMESPACE_BLOCK", "TYPE_DECL", "METHOD", "MEMBER"],
                    ["FILE"],
                    "base"),
                new CpgEdgeSchema(
                    "CONTAINS",
                    "ContainsEdge",
                    ["FILE", "NAMESPACE", "TYPE_DECL", "METHOD", "BLOCK"],
                    ["NAMESPACE_BLOCK", "TYPE_DECL", "MEMBER", "METHOD", "METHOD_PARAMETER_IN", "METHOD_PARAMETER_OUT", "METHOD_RETURN", "LOCAL", "BLOCK", "CALL", "CONTROL_STRUCTURE", "RETURN", "TYPE_REF", "IDENTIFIER", "LITERAL", "FIELD_IDENTIFIER", "METHOD_REF"],
                    "base"),
                new CpgEdgeSchema(
                    "EVAL_TYPE",
                    "EvalTypeEdge",
                    ["CALL", "IDENTIFIER", "LITERAL", "FIELD_IDENTIFIER", "METHOD_REF"],
                    ["TYPE"],
                    "base"),
                new CpgEdgeSchema(
                    "CFG",
                    "CfgEdge",
                    ["METHOD", "METHOD_PARAMETER_IN", "METHOD_PARAMETER_OUT", "METHOD_RETURN", "LOCAL", "CALL", "CONTROL_STRUCTURE", "RETURN"],
                    ["METHOD", "METHOD_PARAMETER_IN", "METHOD_PARAMETER_OUT", "METHOD_RETURN", "LOCAL", "CALL", "CONTROL_STRUCTURE", "RETURN"],
                    "controlflow"),
                new CpgEdgeSchema(
                    "DOMINATE",
                    "DominateEdge",
                    ["METHOD", "METHOD_PARAMETER_IN", "METHOD_PARAMETER_OUT", "METHOD_RETURN", "LOCAL", "CALL", "CONTROL_STRUCTURE", "RETURN"],
