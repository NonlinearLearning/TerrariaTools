{
    public static CpgSchema Create()
    {
        return new CpgSchema(
            Layers:
            [
                new CpgLayerSchema("base", "Base layer."),
                new CpgLayerSchema("controlflow", "Control flow layer.", ["base"]),
                new CpgLayerSchema("typerel", "Type relations layer.", ["base"]),
                new CpgLayerSchema("callgraph", "Call graph layer.", ["typerel"]),
            ],
            Nodes:
            [
                new CpgNodeSchema(
                    "META_DATA",
                    "MetaDataNode",
                    "StoredNode",
                    [],
                    ["LANGUAGE", "OVERLAYS", "ROOT", "VERSION"],
                    "base"),
                new CpgNodeSchema(
                    "NAMESPACE",
                    "NamespaceNode",
                    "StoredNode",
                    [],
                    ["NAME", "FULL_NAME"],
                    "base"),
                new CpgNodeSchema(
                    "NAMESPACE_BLOCK",
                    "NamespaceBlockNode",
                    "AstNode",
                    [],
                    ["NAME", "FULL_NAME"],
                    "base"),
                new CpgNodeSchema(
                    "FILE",
                    "FileNode",
                    "StoredNode",
                    [],
                    ["PATH", "NAME"],
                    "base"),
                new CpgNodeSchema(
                    "METHOD",
                    "MethodNode",
                    "AstNode",
                    ["ICfgNode", "IDeclarationNode"],
                    ["NAME", "CONTAINING_TYPE_NAME", "RETURN_TYPE_NAME", "FULL_NAME", "SIGNATURE", "TYPE_FULL_NAME"],
                    "base"),
                new CpgNodeSchema(
                    "MEMBER",
                    "MemberNode",
                    "AstNode",
                    ["IDeclarationNode"],
                    ["NAME", "TYPE_FULL_NAME", "CONTAINING_TYPE_NAME"],
                    "base"),
                new CpgNodeSchema(
                    "METHOD_PARAMETER_IN",
                    "MethodParameterInNode",
                    "AstNode",
                    ["ICfgNode", "IDeclarationNode"],
                    ["METHOD_NAME", "NAME", "ORDER", "TYPE_FULL_NAME", "CONTAINING_TYPE_NAME"],
                    "base"),
                new CpgNodeSchema(
                    "METHOD_PARAMETER_OUT",
                    "MethodParameterOutNode",
                    "AstNode",
                    ["ICfgNode", "IDeclarationNode"],
                    ["METHOD_NAME", "NAME", "ORDER", "TYPE_FULL_NAME", "CONTAINING_TYPE_NAME"],
                    "base"),
                new CpgNodeSchema(
                    "METHOD_RETURN",
                    "MethodReturnNode",
                    "AstNode",
                    ["ICfgNode"],
                    ["METHOD_NAME", "TYPE_FULL_NAME", "CONTAINING_TYPE_NAME"],
                    "base"),
                new CpgNodeSchema(
                    "LOCAL",
                    "LocalNode",
                    "AstNode",
                    ["ICfgNode", "IDeclarationNode"],
                    ["METHOD_NAME", "NAME", "ORDER", "TYPE_FULL_NAME", "CONTAINING_TYPE_NAME"],
                    "base"),
                new CpgNodeSchema(
                    "TYPE",
                    "TypeNode",
                    "StoredNode",
                    [],
                    ["FULL_NAME"],
                    "base"),
                new CpgNodeSchema(
                    "TYPE_REF",
                    "TypeRefNode",
                    "AstNode",
                    [],
                    ["TYPE_FULL_NAME"],
                    "base"),
                new CpgNodeSchema(
                    "TYPE_DECL",
                    "TypeDeclNode",
                    "AstNode",
                    [],
                    ["NAME", "BASE_TYPE_NAME", "FULL_NAME"],
                    "base"),
                new CpgNodeSchema(
                    "BLOCK",
                    "BlockNode",
                    "AstNode",
                    [],
                    ["METHOD_NAME", "CONTAINING_TYPE_NAME"],
                    "base"),
                new CpgNodeSchema(
                    "CALL",
                    "CallNode",
                    "ExpressionNode",
                    [],
                    ["OWNER_METHOD_NAME", "TARGET_METHOD_NAME", "ORDER", "CONTAINING_TYPE_NAME", "TYPE_FULL_NAME", "RESOLVED_TARGET_METHOD_ID", "METHOD_FULL_NAME"],
                    "base"),
                new CpgNodeSchema(
                    "CONTROL_STRUCTURE",
                    "ControlStructureNode",
                    "AstNode",
                    ["ICfgNode"],
                    ["METHOD_NAME", "CONTROL_STRUCTURE_TYPE", "ORDER", "CONTAINING_TYPE_NAME"],
                    "base"),
                new CpgNodeSchema(
                    "RETURN",
                    "ReturnNode",
                    "AstNode",
                    ["ICfgNode"],
                    ["METHOD_NAME", "ORDER", "CONTAINING_TYPE_NAME"],
                    "base"),
                new CpgNodeSchema(
                    "FIELD_IDENTIFIER",
                    "FieldIdentifierNode",
                    "ExpressionNode",
                    [],
                    ["NAME", "TYPE_FULL_NAME"],
                    "base"),
                new CpgNodeSchema(
                    "METHOD_REF",
                    "MethodRefNode",
                    "ExpressionNode",
                    [],
                    ["METHOD_NAME", "TYPE_FULL_NAME"],
                    "base"),
                new CpgNodeSchema(
                    "IDENTIFIER",
                    "IdentifierNode",
                    "ExpressionNode",
                    [],
                    ["NAME", "ORDER", "TYPE_FULL_NAME"],
                    "base"),
                new CpgNodeSchema(
                    "LITERAL",
                    "LiteralNode",
                    "ExpressionNode",
                    [],
                    ["CODE", "ORDER", "TYPE_FULL_NAME"],
                    "base"),
            ],
